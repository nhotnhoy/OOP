    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int a = JOptionPane.showConfirmDialog(null, "Xác nhận đổi mật khẩu?", "Thoát", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
        if (!jTextField3.getText().equals(jTextField4.getText())) {
            JOptionPane.showMessageDialog(this, "Mời nhập lại!?!");
            return;
        }
        Connection con = DBConnect.getJDBCConnection();
        try {
            PreparedStatement st = con.prepareStatement("UPDATE account SET password=? WHERE usename=?;");
            st.setString(2, jLabel15.getText());
            st.setString(1, jTextField4.getText());
            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Thành Công!!");
            con.close();

        } catch (SQLException ex) {
            Logger.getLogger(Account.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Thông tin chưa chính xác!!!");
        }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

