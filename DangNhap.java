    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Connection con = DBConnect.getJDBCConnection();
        PreparedStatement ps, ps1;
        try {
            ps = con.prepareStatement("SELECT * FROM oop.account WHERE usename=? AND password=?");
            ps.setString(1, jTextField1.getText());
            ps.setString(2, jPasswordField1.getText());
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                ps1 = con.prepareStatement("select * from oop.account,oop.nhanvien where oop.account.idnv=oop.nhanvien.idnv and usename=?");
                ps1.setString(1, jTextField1.getText());
                ResultSet rs1 = ps1.executeQuery();
                rs1.next();
                String chucvu = rs1.getString("chucvu");
                if ("quanli".equals(chucvu)) {
                    setVisible(false);
                    new GDQuanLi(rs1.getString("hoten")).setVisible(true);
                }
                if ("admin".equals(chucvu)) {
                    setVisible(false);
                    new GDAdmin(rs1.getString("hoten")).setVisible(true);
                }
                if ("nhapkho".equals(chucvu)) {
                    setVisible(false);
                    new GDNhanVienNhap(rs1.getString("hoten")).setVisible(true);
                }
                if ("xuatkho".equals(chucvu)) {
                    setVisible(false);
                    new GDNhanVienXuat(rs1.getString("hoten")).setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Tài khoản hoặc mật khẩu không đúng", null, JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DangNhap.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
